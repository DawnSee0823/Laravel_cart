            <div class="content">
                <p>Ziema mums asociējas ar mājīgu Ziemassvētku atmosfēru, kalnu pārgājieniem un slēpošanu, kā arī apgrūtinātu braukšanu, ja laika apstākļi ir patiešām ziemai raksturīgi. Par laimi, pēdējās desmitgadēs ir strauji attīstījušās automobiļu drošības sistēmas, kas ievērojami palielina satiksmes drošību pat uz slidena ceļa.</p>

                <p>Šīs palīdzības sistēmas, piemēram, ABS (riteņu pretbloķēšanas sistēma) un ESP (elektroniskā stabilitātes sistēma), sāk darboties automātiski, kad automašīna zaudē saķeri, un ir labs palīgs, ja vien arī autovadītājs zina, kas ir jādara. Atcerieties, ka šīs sistēmas ir domātas, lai jums palīdzētu, taču tās nepaveiks darbu jūsu vietā. Tāpēc labākais veids, kā tās izmantot pilnībā, lai braukšana būtu pēc iespējas drošāka, ir iepazīt savu transportlīdzekli un šo drošības sistēmu darbību. Vai varat pārliecinoši teikt, ka pārzināt tās?</p>

                <br>
                <br>

                <h2>Elektroniskās drošības sistēmas autovadītāja atbalstam</h2>

                <p>Vecākos transportlīdzekļos mēs iemācījāmies izmantot sajūga pedāli, taču tā nav labākā rīcība, ja jūs zaudējat saķeri stūrējot modernu automašīnu. Ja jūsu automašīna ir aprīkota ar elektroniskās drošības sistēmām, tad drīzāk ir jāatlaiž gāzes pedālis un jātur priekšējie riteņi vēlamajā virzienā. Dažām no šīm jaunajām sistēmām ir nepieciešama nedaudz atšķirīga pieeja nekā iepriekš, tāpēc ir ļoti svarīgi labi iepazīt savu automašīnu un iemācīties rīkoties ar jauno tehnoloģiju.</p>

                <br>

                <h3>ABS</h3>

                <p>ABS (Anti-Blocking System) ir riteņu pretbloķēšanas sistēma. Tā palīdz automašīnas riepām saglabāt saķeri ar ceļu, kamēr jūs bremzējat, vai citiem vārdiem sakot, palīdz izvairīties no riteņu slīdēšanas, sadalot bremzēšanas spēku uz visiem riteņiem. Ja automašīnai nav ABS, automašīna slīdēs taisni uz priekšu, un jūs nevarēsiet izvairīties no šķēršļiem. </p>

                <p>ABS, izmantojot sensorus, nosaka, vai ritenis bremzēšanas gadījumā gatavojas bloķēties. Ieslēdzoties pretbloķēšanas bremžu sistēmai, ir jūtama pulsēšana vai vibrācija uz bremžu pedāļa un ir dzirdams neliels troksnis. Daudzi cilvēki, kad tas notiek, nobīstas un atlaiž bremzes, taču patiesībā vajadzētu turēt kāju stingri uz bremžu pedāļa, līdz automašīna pilnībā apstājas.</p>

                <p>Droša braukšana ar transportlīdzekli, kas aprīkots ar ABS, un pilnīga ABS sistēmas izmantošana var būt diezgan sarežģīta cilvēkiem, kuri ir mācījušies braukt ar vecākām automašīnām. Nonākot bīstamā situācijā, tā vietā, lai piespiestu un atlaistu bremžu pedāli vairākas reizes, tādejādi novēršot bremžu bloķēšanos, jums ir jātur pedālis vienmērīgi nospiests. Citiem vārdiem sakot, ļaujiet ABS sistēmai kontrolēt bremzēšanu, kamēr jūs rūpējaties par stūrēšanu.</p>

                <p><img src="/images/front/single-post-content-image.jpg" alt=""></p>

                <h3>Kā apstāties ar ABS uz slidena ceļa</h3>

                <p>ABS faktiski palielina apstāšanās attālumus uz sniegotām virsmām, kā arī uz grants vai smiltīm. Bez ABS bloķētas riepas ierokas sniegā un tādejādi, virzot sniegu uz priekšu, izveido ķīli riepas priekšā. Šis ķīlis palīdz apturēt automašīnu. Izmantojot ABS, vadītājs var vieglāk atgūt kontroli pār automašīnu, bet apstāšanās distance faktiski palielināsies, jo ķīlis riepas priekšā neveidojas.</p>

                <p>Uz sniegotiem ceļiem, labākais padoms ir ievērot distanci starp jūsu un priekšā braucošo automašīnu un braukt lēni. Viegli uzspiediet uz bremzes pedāļa, izvairoties no ABS iedarbināšanas. Uz daļēji apledojušiem ceļiem nekad nespiediet bremžu pedāli atkārtoti vairākas reizes pēc kārtas. Bremžu pedālis ir jānospiež vienreiz un vienmērīgi, tajā pašā laikā jāskatās, kur stūrējat automašīnu, lai varat to vadīt pareizajā virzienā. Nespiediet bremžu pedāli strauji, jo tas var bloķēt bremzes un izraisīt riteņu pretbloķēšanas sistēmas efektivitātes zaudēšanu.</p>

                <p>Ja ceļš ir pilnībā apledojis, var gadīties, ka ABS neieslēgsies un riteņi bloķēsies. Ja tā ir noticis, lai apstātos, atlaidiet gāzes pedāli un uz brīdi iztaisnojiet stūri, lai riepas varētu atgūt saķeri. Ja tas nepalīdz, jums būs viegli jānospiež un jāatlaiž bremzes pedālis vairākas reizes.</p>
