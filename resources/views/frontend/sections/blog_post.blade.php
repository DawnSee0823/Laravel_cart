                <h1 class="post-heading">Kā braukt ziemā: padomi ABS, ESP un TCS sistēmu izmantošanai</h1>
                    
                <div class="featured-image">
                    <img src="/images/front/single-post-featured-image@1.5.jpg" alt="">
